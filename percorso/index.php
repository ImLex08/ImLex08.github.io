   <div class="section" id="parallel-paths">
      <h1>Percorsi Paralleli</h1>
      <p>
         Dopo intense discussioni e nottate passate a confrontare documentazione, video e articoli, prendemmo una decisione strategica. Invece di imparare entrambi la stessa tecnologia, decidemmo di dividerci i compiti per coprire più terreno e specializzarci. <br> <br>
         Il mio amico, affascinato dalla potenza, dal vasto ecosistema e dalla popolarità di <a href="https://it.wikipedia.org/wiki/React_(web_framework)">React</a> nel mondo del lavoro, decise di concentrarsi anima e corpo su <a href="https://nextjs.org/">Next.js</a> <br> <br>
         Io, d'altra parte, fui conquistato dalla filosofia di <a href="https://it.wikipedia.org/wiki/Vue.js">Vue</a>, dalla sua sintassi elegante e dalla sua curva di apprendimento che percepii come più dolce e progressiva. <a href="https://nuxt.com/">Nuxt</a> mi sembrò subito incredibilmente intuitivo, ben strutturato e perfetto per costruire interfacce utente reattive con piacere e rapidità. <br> <br>
         Questa divisione si è rivelata una strategia vincente: pur lavorando su <a href="https://it.wikipedia.org/wiki/Framework">Framework</a> diversi, i concetti di base rimanevano gli stessi. Questo ci permette di scambiarci consigli, risolvere problemi a vicenda e, di fatto, imparare due tecnologie quasi contemporaneamente.
      </p>

   </div>
